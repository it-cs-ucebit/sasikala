import math
print ("abs(100.12)", abs(100.12)) #This method returns absolute value of x.
print ("math.ceil(100.12) : ", math.ceil(100.12))#This method returns smallest integer not less than x.
print ("math.exp(100.12) : ", math.exp(100.12))#Returns exponential value
print ("math.fabs(100.12) : ", math.fabs(100.12)) #Returns Absolute value
print ("math.floor(100.12) : ", math.floor(100.12)) # returns largest integer not greater than x.
print ("math.log(100.12) : ", math.log(100.12)) #returns natural logarithm of x, for x > 0.
print ("math.log10(100.12) : ", math.log10(100.12)) #returns base-10 logarithm of x for x > 0
print ("max(80, 100, 1000) : ", max(80, 100, 1000)) #returns largest of its arguments.
print ("min(80, 100, 1000) : ", min(80, 100, 1000)) #returns smallest of its arguments.
print ("math.modf(100.12) : ", math.modf(100.12)) #returns the fractional and integer parts of x in a two-item tuple.
print ("math.pow(100, 2) : ", math.pow(100, 2)) #This method returns value of x power y
print ("round(80.23456, 2) : ", round(80.23456, 2)) #This method returns x rounded to n digits from the decimal point.
print ("math.sqrt(100) : ", math.sqrt(100)) #This method returns square root of x for x > 0.

 

